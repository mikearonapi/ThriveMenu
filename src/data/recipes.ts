// Helper functions
export function getRecipeById(id: string): Recipe | undefined {
  return allRecipes.find((recipe) => recipe.id === id);
}

export function getRecipesByMealType(mealType: string): Recipe[] {
  return allRecipes.filter((recipe) => recipe.mealType === mealType);
}

export function getRecipesByCategory(category: string): Recipe[] {
  return allRecipes.filter((recipe) => recipe.category === category);
}

export function searchRecipes(query: string): Recipe[] {
  const lowerQuery = query.toLowerCase();
  return allRecipes.filter(
    (recipe) =>
      recipe.name.toLowerCase().includes(lowerQuery) ||
      recipe.description.toLowerCase().includes(lowerQuery) ||
      recipe.category.toLowerCase().includes(lowerQuery) ||
      recipe.healthBenefits.toLowerCase().includes(lowerQuery)
  );
}

export function getQuickRecipes(): Recipe[] {
  return allRecipes.filter((recipe) => recipe.isQuick || (recipe.totalTime && recipe.totalTime <= 30));
}

export function getKidFriendlyRecipes(): Recipe[] {
  return allRecipes.filter((recipe) => recipe.isKidFriendly);
}

export function getOmega3Recipes(): Recipe[] {
  return allRecipes.filter((recipe) => recipe.hasOmega3);
}

export function getHeartHealthyRecipes(): Recipe[] {
  return allRecipes.filter((recipe) => recipe.isHeartHealthy || recipe.hasHighFiber);
}

export function getAntiInflammatoryRecipes(): Recipe[] {
  return allRecipes.filter((recipe) => recipe.isAntiInflammatory);
}

